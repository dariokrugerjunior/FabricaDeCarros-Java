package modelo;

public class Carros {

	protected String carroModelo;
	protected String carroCor;
	protected int carroPortas;

	public String getCarroModelo() {
		return carroModelo;
	}

	public void setCarroModelo(String carroModelo) {
		this.carroModelo = carroModelo;
	}

	public String getCarroCor() {
		return carroCor;
	}

	public void setCarroCor(String carroCor) {
		this.carroCor = carroCor;
	}

	public int getCarroPortas() {
		return carroPortas;
	}

	public void setCarroPortas(int carroPortas) {
		this.carroPortas = carroPortas;
	}

}
